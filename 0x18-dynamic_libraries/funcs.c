#include <stdio.h>

/**
 * add - addition
 * @a: num 1
 * @b: num 2
 * Return:  ad result
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substract
 * @a: num 1
 * @b: num 2
 * Return:  add
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - modulus
 * @a: num 1
 * @b: num 2
 * Return:  sub result
*/

int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - divide
 * @a: num 1
 * @b: num 2
 * Return: div result
*/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multipy
 * @a: num 1
 * @b: num 2
 * Return: mul result
*/

int mul(int a, int b)
{
	return (a * b);
}
